class TestTcpStreamReassembly:
    """Verify retransmission-aware TCP reconstruction."""

    def _stream(self, segments):
        s = _TcpStream("1.2.3.4", "5.6.7.8", 1234, 80)
        s.segments = [
            _Segment(seq=seq, payload=payload, timestamp=0.0, frame_number=i)
            for i, (seq, payload) in enumerate(segments)
        ]
        return s

    def test_in_order(self):
        s = self._stream([(0, b"hello"), (5, b" world")])
        assert s.ordered_payload() == b"hello world"

    def test_out_of_order(self):
        s = self._stream([(5, b" world"), (0, b"hello")])
        assert s.ordered_payload() == b"hello world"

    def test_retransmission_deduplicated(self):
        # Segment at seq=0 sent twice — payload must appear once
        s = self._stream([(0, b"hello"), (0, b"hello"), (5, b" world")])
        assert s.ordered_payload() == b"hello world"
        assert s.retransmitted_bytes == 5

    def test_partial_overlap(self):
        # First segment: bytes 0-4, second: bytes 3-7 (overlap at 3-4)
        s = self._stream([(0, b"hello"), (3, b"lo wo"), (8, b"rld")])
        assert s.ordered_payload() == b"hello world"
        assert s.overlapping_bytes == 2

    def test_gap_filled_with_zeros(self):
        # Gap between seq 5 and seq 10
        s = self._stream([(0, b"hello"), (10, b"world")])
        payload = s.ordered_payload()
        assert payload[:5] == b"hello"
        assert payload[5:10] == b"\x00" * 5
        assert payload[10:] == b"world"

    def test_empty_stream(self):
        s = _TcpStream("1.2.3.4", "5.6.7.8", 1234, 80)
        assert s.ordered_payload() == b""



    """Verify direction is determined from the camera endpoint, not address sorting."""

    def test_camera_sorts_before_client(self):
        """Camera IP 10.0.0.1 sorts before client 192.168.1.100 lexically.
        The old code would have put camera→client in 'forward' (requests),
        which is backwards.  The new code always uses camera endpoint."""
        # The good PCAP has camera=192.168.1.100, protect=10.54.4.1
        # 10.54.4.1 < 192.168.1.100 lexically, so old code put protect→camera
        # in 'reverse' (responses) — correct by accident.
        # We verify the result is still correct with the new code.
        txns = reconstruct_streams(
            _good_pcap(),
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert len(txns) >= 1
        # Request must come FROM protect, response FROM camera
        txn = txns[0]
        assert txn.src_ip == PROTECT_IP
        assert txn.dst_ip == CAMERA_IP

    def test_response_body_contains_soap_not_request(self):
        """The response body must contain PullMessagesResponse, not PullMessages."""
        txns = reconstruct_streams(
            _good_pcap(),
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert b"PullMessagesResponse" in txns[0].response_body



    def setup_method(self):
        self.transactions = reconstruct_streams(
            _good_pcap(),
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )

    def test_at_least_one_transaction(self):
        assert len(self.transactions) >= 1

    def test_transaction_type(self):
        assert isinstance(self.transactions[0], HttpTransaction)

    def test_src_dst_ips(self):
        txn = self.transactions[0]
        assert PROTECT_IP in (txn.src_ip, txn.dst_ip)
        assert CAMERA_IP in (txn.src_ip, txn.dst_ip)

    def test_http_status_200(self):
        assert self.transactions[0].http_status == 200

    def test_response_body_is_soap(self):
        assert _is_soap(self.transactions[0].response_body)

    def test_extract_transactions_returns_pull_transaction(self):
        pull_txns, faults = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert len(pull_txns) >= 1
        assert faults == []

    def test_notification_extracted(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert any(len(t.notifications) > 0 for t in pull_txns)

    def test_is_motion_true(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        events = [n for t in pull_txns for n in t.notifications]
        assert any(e.is_motion is True for e in events)

    def test_source_is_protect(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert all(t.source == EventSource.PROTECT for t in pull_txns)

    def test_provenance_fields(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        txn = pull_txns[0]
        assert txn.tcp_stream >= 0
        assert txn.request_frame > 0
        assert txn.response_frame > 0

    def test_raw_bodies_retained(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        txn = pull_txns[0]
        assert len(txn.raw_response) > 0


