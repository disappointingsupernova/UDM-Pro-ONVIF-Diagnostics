class TestFaultPcap:
    def setup_method(self):
        self.transactions = reconstruct_streams(
            _fault_pcap(),
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )

    def test_at_least_one_transaction(self):
        assert len(self.transactions) >= 1

    def test_http_status_500(self):
        assert self.transactions[0].http_status == 500

    def test_fault_extracted(self):
        _, faults = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert len(faults) >= 1

    def test_fault_code(self):
        _, faults = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert "Receiver" in faults[0].code

    def test_fault_subcode(self):
        _, faults = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert faults[0].subcode is not None

    def test_pull_transaction_has_fault(self):
        pull_txns, _ = extract_transactions(
            self.transactions,
            camera_ip=CAMERA_IP,
            protect_ip=PROTECT_IP,
        )
        assert any(t.soap_fault is not None for t in pull_txns)
